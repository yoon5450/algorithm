/*

문제 : 가계부 정리

✔️ 문제 설명

당신은 가계부를 정리하려고 합니다. 가계부에는 금액과 플래그(a, s)가 번갈아가며 기록되어 있습니다.

- 초기 자산은 0입니다.
- 금액 다음에는 반드시 하나의 플래그가 옵니다.  ex) 23000a
- 플래그 a는 수입을 의미합니다.
- 플래그 s는 지출을 의미합니다.
- 이러한 금액, 플래그 쌍이 문자열 전체에서 반복됩니다.

주어진 가계부 기록을 보고 총 자산을 계산하는 함수를 작성하세요!

🤔 제한조건

- 문자열은 숫자와 a, s로만 이루어져 있습니다.
- 숫자는 양수만 주어집니다.
- 문자열의 길이는 2이상 100이하입니다.
- 유효하지 않은 입력은 주어지지 않습니다. (숫자 없이 플래그만 오거나, 플래그 없이 끝나는 경우는 없음)


*/

// -----------------------------------------------------------------------------------------

/*

풀이 : 문제 풀이 
*/

// solution 함수 선언, 정답 리턴이라는 구조는 바꾸시면 안 됩니다.
/* 아래쪽 테이블에 console.log() 출력을 가로채 출력하도록 했기 때문에 
   출력창에 에러, 출력 모두 나올 겁니다.*/

export function solution1(str) {
  let result = 0;
  let num = '';

  for (let i = 0; i < str.length; i++) {
    const char = str[i];

    if (char === 'a' || char === 's') {
      const value = Number(num);
      result += char === 'a' ? value : -value;
      num = '';
    } else {
      num += char;
    }
  }

  return result;
}
